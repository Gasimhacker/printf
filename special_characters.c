#include "main.h"
#include <stdarg.h>

/**
 * print_back_slash - Prints a backslash 
 * 
 * Return: Nothing
 */
void print_back_slash()
{
    _putchar('\\');
}

/**
 * print_double_quotes - Prints a double quote
 *
 * Return: Nothing
 */
void print_double_quotes()
{
    _putchar('\"');
}

/**
 * print_percent - Prints a percent sign
 *
 * Return: Nothing
 */
void print_percent()
{
    _putchar('%%');
}

/**
 * print_newline - Prints a new line
 *
 * Return: Nothing
 */
void print_newline()
{
    _putchar('\n');
}